<p>三、定投业务提示</p>
<p>1、投资者在汇成基金办理上述基金定投业务，每期最低扣款金额为100元人民币（含定投申购费）。</p>
<p>2、投资者应当充分了解基金定期定额投资和零存整取等储蓄方式的区别。定期定额投资是引导投资者进行长期投资、平均投资成本的一种简单易行的投资方式。但是定期定额投资并不能规避基金投资所固有的风险，不能保证投资者获得收益，也不是替代储蓄的等效理财方式。</p>
<p>四、费率优惠活动</p>
